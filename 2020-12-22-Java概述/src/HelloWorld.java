// Ctrl+鼠标左键，点击，可以跳到对用的源码位置
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World");
    }

}
// 类（class）： public class HelloWorld {类的内部}类的外部
// 方法不能出现在类的外部  方法（method）==函数（function）
// 方法（method）： public static void main(String[] args) {方法内部}方法外部
// 语句只能出现在方法内部*
// 语句（statement）： System.out.println("Hello World");

// public class 类名称 {}
// 类名称：1.名词 2.尽量用英文 3.每个单词首字母大写 4.类名和文件名一致

// 方法
// public static void 方法名(String[] args) {}
// 方法名：1.动词 2.尽量用英文 3.第一个单词全小写，以后的单词首字母大写